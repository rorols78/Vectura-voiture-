    <h2>
        Lois applicables
    </h2>
    Conformément au Règlement général sur la protection des données (RGPD), cette politque de confidentialité est conforme aux règlements suivants.
    <br><br>
    Les données à caractère personnel doivent être :
    <br><br>
    a. traitées de manière licite, loyale et transparente au regard de la personne concernée (licéité, loyauté, transparence);
    <br><br>
    b. collectées pour des finalités déterminées, explicites et légitimes, et ne pas être traitées ultérieurement d'une manière incompatible
    avec ces finalités; le traitement utlérieur à des fins archivistiques dans l'intérêt public, à des fins de recherche scientifique ou
    historique ou à des fins statistiques n'est pas considéré, conformément à l'article 89, paragraphe 1, comme incompatible avec les
    finalités initiales (limitation des finalités);
    <br><br>
    c. adéquates, pertinentes et limitées à ce qui est nécessaire au regard des finalités pour lesquelles elles sont traitées (minimisation des
    données);
    <br><br>
    d. conservées sous une forme permettant l'identification des personnes concernées pendant une durée n'excédant pas celle nécessaire
    au regard des finalités pour lesquelles elles sont traitées; les données à caractère personnel peuvent être conservées pour des durées
    plus longues dans la mesure où elles seront traitées exclusivement à des fins archiviques dans l'intérêt public, à des fins de
    recherche scientifique ou historique ou à des fins statistiques conformément à l'article 89, paragraphe 1, pour autant que soient
    mises en oeuvre les mesures techniques et organisationnelles appropriées requises par le règlement afin de garantir les droits et
    libertés de la personne concernée (limitation de la conservation);
    <br><br>
    f. traitées de façon à garantir une sécurité appropriée des données à caractère personnel, y compris la protection contre le traitement
    non autorisé ou illicite et contre la perte, la destruction ou les dégâts d'origine accidentelle, à l'aide de mesure techniques ou
    organisationnelles appropriées (intégrité et confidentialité).
    <br><br>
    Le traitement n'est licite que si, et dans la mesure où, au moins une des conditions suivantes est remplie :
    <br><br>
    a. la personne concernée a consenti au traitement de ses données à caractère personnel pour une ou plusieurs finalités spécifiques ;
    <br><br>
    b. le traitement est nécessaire à l'exécution d'un contrat auquel la personne concernée est partie ou à l'exécution de mesures
    précontractuelles prises à la demande de celle-ci ;
    <br><br>
    c. le traitement est nécessaire au respect d'une obligaitonlégale à laquelle le responsable du traitement est soumis ;
    <br><br>
    d. le traitement est nécessaire à la sauvegarde des intérêts vitaux de la personne concernée ou d'une autre personne physique ;
    <br><br>
    e. le traitement est nécessaire à l'exécution d'une mission d'intérêt public ou relevant de l'exercice de l'autorité publique dont est
    investi le responsable du traitement ;
    <br><br>
    f. le traitement est nécessaire aux fins des intérêts légitimes poursuivis par le responsable du traitement ou par un tiers, à moins que
    ne prévalent les intérêts ou les libertés et droits fondamentaux de la personne concernée qui exigent une protection des données à
    caractère personnel, notamment lorsque la personne concernée est un enfant.
    <br><br>
    Pour les résidents de l'Etat de Californie, cette politique de confidentialité vise à se conformer à la Califoria Consumer Privacy Act
